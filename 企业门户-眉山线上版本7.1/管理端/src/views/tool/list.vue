<template>
  <!--视图容器-->
  <el-container>
    <!--主要引用页面-->
    <el-main>
      <div ref="formBox">
        <el-form ref="params" :inline="true" :model="params" size="small">
          <el-form-item label="工具名称" prop="title">
            <el-input v-model="params.title" clearable placeholder="请输入工具名称" />
          </el-form-item>
          <el-form-item label="年份" prop="year">
            <el-date-picker v-model="params.year" type="year" value-format="yyyy-MM-dd" placeholder="选择年" />
          </el-form-item>
          <el-form-item>
            <el-button type="primary" @click="searchClick">查 询</el-button>
            <el-button type="primary" @click="reloadClick('params')" plain>重 置</el-button>
          </el-form-item>
        </el-form>
      </div>
      <el-table v-loading="listLoading" element-loading-text="拼命加载中" stripe :data="tableData" border style="width: 100%"
        :header-cell-style="{background:'#F8F8F9',color:'#606266'}" :height="tableHeight">
        <el-table-column type="index" align="center" label="序号" width="60" />
        <el-table-column align="left" prop="title" label="工具名称" width="200" />
        <el-table-column align="left" prop="tooltype" label="工具类型" width="120" />
        <el-table-column align="left" prop="description" show-overflow-tooltip label="简介" />
        <el-table-column align="left" prop="toolsize" label="文件大小" width="100" />
        <el-table-column align="left" :formatter="dateFormatter" label="发布时间" width="200" />
        <el-table-column label="操作" align="left" width="200">
          <template slot-scope="scope">
            <!-- <router-link :to="{path: 'news-edit', query: {id: scope.row.id}}"><el-button type="success" size="mini">编辑</el-button></router-link> -->
            <el-button type="danger" size="mini" @click="handleDelete(scope.row.id)">删除</el-button>
          </template>
        </el-table-column>
      </el-table>
      <el-pagination layout="total, sizes, prev, pager, next, jumper" background :total="total" :page-size="params.size"
        :current-page="params.page" :page-sizes="[15, 20, 25, 30]" style="margin-top:20px;"
        @current-change="handleCurrentChange" @size-change="handleSizeChange" />
    </el-main>
    <!--主要引用页面结束-->
  </el-container>
  <!--内容结束-->
</template>
<script>
import { parseTime } from '@/utils/index'
import { getToolList, deleteTool } from '@/api/tool'
import { msgAutoClose, msgNoClose } from "@/utils/element";
export default {
  name: 'LibraryList',
  data () {
    return {
      tableHeight: null,
      tableData: [],
      params: {
        title: '',
        year: '',
        page: 1,
        size: 15
      },
      total: 0,
      listLoading: false
    }
  },
  mounted () {
    this.tableHeight = window.innerHeight - this.$refs.formBox.offsetHeight - 200
    window.onresize = () => {
      return (() => {
        this.tableHeight = window.innerHeight - this.$refs.formBox.offsetHeight - 200
      })()
    }
  },
  created () {
    this.getList()
  },
  methods: {
    getList () {
      this.listLoading = true
      getToolList(this.params).then(res => {
        this.listLoading = false
        if (res.success && res.data) {
          this.tableData = res.data.rows
          this.total = res.data.total
        } else {
          this.total = 0
          this.tableData = []
        }
      }).catch(() => {
        this.listLoading = false
      })
    },
    dateFormatter (row, column) {
      if (row.time) {
        return parseTime(row.time)
      } else {
        return ''
      }
    },
    handleCurrentChange (val) {
      this.params.page = val
      this.getList()
    },
    handleSizeChange (val) {
      this.params.size = val
      this.getList()
    },
    handleDelete (id) {
      this.$confirm('此操作将永久删除该条内容, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        deleteTool(id).then(res => {
          msgAutoClose({
            message: '删除成功！',
            type: "success"
          });
          this.getList()
        })
      })
    },
    searchClick () {
      this.getList()
    },
    reloadClick (formName) {
      this.$refs[formName].resetFields()
      this.getList()
    }
  }
}
</script>
